-- ---------------------------------------------------------------------------
-- 0. Role helper: principal check used by all security policies
-- ---------------------------------------------------------------------------
create or replace function is_principal()
returns boolean language sql stable as
$$ select coalesce(auth.jwt()->>'email','') = 'principal@hostel.app' $$;

