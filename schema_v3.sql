-- ---------------------------------------------------------------------------
-- 3. Row Level Security — real enforcement, not just hidden buttons
--    Principal: full access everywhere.
--    Warden: can READ and ADD entry records; can update attendance (re-save)
--            and outpass returns; CANNOT touch the students table at all,
--            and cannot edit or delete history.
-- ---------------------------------------------------------------------------
alter table students     enable row level security;
alter table attendance   enable row level security;
alter table fines        enable row level security;
alter table medical      enable row level security;
alter table leaves       enable row level security;
alter table outpass      enable row level security;
alter table discipline   enable row level security;
alter table complaints   enable row level security;
alter table parent_calls enable row level security;

-- Remove EVERY existing policy on our tables (whatever earlier setup scripts named them),
-- then recreate the correct role-aware set below. This guarantees no old permissive
-- policy survives to give wardens more access than intended.
do $$
declare r record;
begin
  for r in
    select policyname, tablename from pg_policies
    where schemaname = 'public'
      and tablename in ('students','attendance','fines','medical','leaves','defaulters',
                        'outpass','discipline','complaints','parent_calls')
  loop
    execute format('drop policy if exists %I on public.%I', r.policyname, r.tablename);
  end loop;
end $$;

-- students: PRINCIPAL ONLY (wardens use the roster function below)
drop policy if exists "principal only" on students;
create policy "principal only" on students
  for all to authenticated using (is_principal()) with check (is_principal());

-- entry tables: everyone signed-in can read + add; edits/deletes controlled
do $$
declare t text;
begin
  foreach t in array array['attendance','fines','medical','leaves','outpass','discipline','complaints','parent_calls'] loop
    execute format('drop policy if exists "read"   on %I', t);
    execute format('drop policy if exists "add"    on %I', t);
    execute format('drop policy if exists "edit"   on %I', t);
    execute format('drop policy if exists "remove" on %I', t);
    execute format('create policy "read" on %I for select to authenticated using (true)', t);
    execute format('create policy "add"  on %I for insert to authenticated with check (true)', t);
    execute format('create policy "remove" on %I for delete to authenticated using (is_principal())', t);
  end loop;
end $$;

-- updates: principal everywhere; warden also on attendance (re-save marks)
-- and outpass (recording the student's return is gate duty)
create policy "edit" on attendance   for update to authenticated using (true) with check (true);
create policy "edit" on outpass      for update to authenticated using (true) with check (true);
create policy "edit" on fines        for update to authenticated using (is_principal()) with check (is_principal());
create policy "edit" on medical      for update to authenticated using (is_principal()) with check (is_principal());
