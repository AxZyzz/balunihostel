-- ---------------------------------------------------------------------------
-- 4. Warden roster — names/wing/class/room ONLY (no address, phones, medical)
--    This is how wardens see the student list without access to private details.
-- ---------------------------------------------------------------------------
create or replace function students_roster()
returns table (id uuid, name text, wing text, class text, room text, bed_no text)
language sql stable security definer set search_path = public as
$$ select id, name, wing, class, room, bed_no from students order by name $$;

revoke all on function students_roster() from public, anon;
grant execute on function students_roster() to authenticated;

